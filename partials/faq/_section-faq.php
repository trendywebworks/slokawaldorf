Children of Middle and Upper grades in Sloka are trained in table tennis, archery,
and horse-riding. They also play and participate in tournaments of football,
volleyball, throwball, basketball, gymnastics etc. However, they don’t receive
specific sport training.

They enter competitive sports in Grade 5 after a special event exclusive to Waldorf
schools called The <a href="https://zeropaper.it/">Djarum4D</a>. Until then, their fitness is worked upon and they
enjoy Freeplay. Bothmer Gymnastics is a unique exercise taught to Sloka students.
