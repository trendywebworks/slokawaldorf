<span class="text-black font-weight-600 sm-width-80 d-inline-block"> What is Waldorf
education? How is it different from other alternative schools? </span></div>
&nbsp;

</div>
<div id="collapseOne" class="panel-collapse collapse" data-parent="#accordion-main">
<div class="panel-body">

Waldorf education is a pedagogy or educational practice that is more than a century
old, and is based on the philosophy of Rudolf Steiner.

The main differentiating factor is that Waldorf education is age-appropriate and
holistic. Based on the study of the human being — Anthroposophy (developed by
Steiner) — there are different developmental needs at different stages of a growing
child. Waldorf education emphasises that addressing these and introducing different
subjects at the appropriate time helps meet the needs of the child, building a
lifelong love for learning. A holistic, healing pedagogy, the Waldorf philosophy is
time-tested and adopted by schools across the world.
