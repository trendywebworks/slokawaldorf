                        <p> An interest in reading German works in the original language led Nirmala, then in college, to a German neighbour, Angelika Mandaiker, who introduced her to Rudolf Steiner's philosophy — Anthroposophy. Years later, an abiding passion for language moved her into a career in advertising as a copywriter, and to later start her own ad agency. Marriage and motherhood followed, and Nirmala saw that education for her own son engaged only the thinking function and was less sensitive to the other aspects such as the emotional intelligence and will of the child. </p>
                        
                        <p> A UNESCO initiative to introduce different educational curricula, including Waldorf, in India, caught her attention, and inspired the idea of an alternative, holistic school. Her deep sensitivity for children and their world, and an unwavering commitment to the task at hand compelled Nirmala Diaz to begin Sloka - The Hyderabad Waldorf School, along with her husband, the late Suresh Kuppu, and their friend Satish Kocharekar. </p>
                            
                        <p> Nirmala continues to be deeply involved in the recruitment and training of teachers to ensure that children have the true Waldorf Experience of child-centred, age-appropriate education. Nirmala remains active in the Waldorf schooling movement in India as well as in Anthroposophy, and is now serving as Secretary of the Anthroposophical Society of India. She loves young people, and is deeply interested to know, understand, and acknowledge their world, and remains the pioneer and conscience-keeper of Sloka, the school she so bravely and lovingly founded. </p>
                        
                            
