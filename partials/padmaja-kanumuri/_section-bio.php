                        <p> Padmaja found Sloka when she was looking for a school which works on the holistic development of the child in a stress-free environment for her three-year-old. As a young mother, she imbibed the Waldorf ways into her parenting, enrolling her second child also at Sloka. She learnt to be patient and not rush anything when working with children. In the meantime, she worked as a lecturer in an Intermediate College and also received a certification from JNTU as a Soft Skills Trainer. She then joined an MNC as a trainer and worked there for two years. </p>

